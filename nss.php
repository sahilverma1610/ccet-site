<a name="AboutIt"><h3 style="font-family:Orbitron">About NSS</h3></a>
<hr>
<p>
National Service Scheme, under the Ministry of Youth Affairs & Sports, Government of India, popularly known as NSS was launched in Gandhiji's Birth Centenary Year 1969.</p>

<a name="Aim"><h3 style="font-family:Orbitron">Aim </h3></a>
<hr>
<p>The main aim of NSS is to develop the personality of students by involving them for community service. The programme aims to inculcate social welfare in students, and to provide service to society without bias. NSS volunteers work to ensure that everyone who is needy gets help to enhance their standard of living and lead a life of dignity. </p>

<a name="Motto"><h3 style="font-family:Orbitron">Motto </h3></a>
<hr>
<p>The Motto of NSS "Not Me But You", reflects the essence of democratic living and upholds the need for self-less service. The philosophy of the NSS is well doctrined in this motto, which underlines on the belief that the welfare of an individual is ultimately dependent on the welfare of the society on the whole and therefore, the NSS volunteers shall strive for the well-being of the society.</p>

<a name="Objectives"><h3 style="font-family:Orbitron">Objectives</h3></a>
<hr>
<p>
The main objectives of National Service Scheme (NSS) are to:<br/>

1.     To understand the community in which they work<br/>
2.     To understand themselves in relation to their community<br/>
3.	To identify the needs and problems of the community and involve them in problem-solving<br/>
4.	To develop among themselves a sense of social and civic responsibility<br/>
5.	To utilize  knowledge in finding practical solutions to individual and community problems<br/>
6.	To develop competence required for group-living and sharing of responsibilities.<br/>
7.	To gain skills in mobilizing community participation.<br/>
8.	To acquire leadership qualities and democratic attitudes.<br/>
9.	To develop capacity to meet emergencies and natural disasters.<br/>
10.	To practice national integration and social harmony.<br/>
</p>



<a name="Activities"><h3 style="font-family:Orbitron">NSS Activities</h3></a>
