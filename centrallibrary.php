To support the academic requirements of the faculty & students, the college has a well stocked library with a collection of about 12000 volumes in Science & Technology, catering to the needs of about 900 members (Both faculty & students) imparting/pursuing studies in four different branches of Engg. The library is continuously in the process to acquire text books & reference books, to keep its reader abreast with the latest information.
The library of the institute aims to facilitate academic & research activities of the faculty & students by providing timely information & resource through cost effective means. The college library is organized into various sections, which are managed by professionally qualified staff.
Library is fully automated and is using Softlink Alice Software and the Barcode system for issue and return will be implemented shortly.
The library is also institutional member of British Library and is entitled to draw 10 books, 5 back issues of the periodicals and 2 video cassettes. British Library also provides information for studying in Britain and about various British examinations like IELTS, BEC, PLAB, YLE etc.
