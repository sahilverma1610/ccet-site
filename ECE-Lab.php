		<center><h1 style="font-family:Orbitron">Electronics and Communication Engineering Department.</h1></center><br>
		
           <h2 style="font-family:Orbitron">LABORATORIES</h2>
		   <hr>
		   <p> 
			<ul>
			
		  <h4><li><b>Analog & Digital Electronics Lab</b></li></h4>

			<p>This lab aims at familarizing the student with the various along communication techniques viz. Amplitude modulation. Frequency modulation and Phase Modulation etc. AM receiver kit is used for demonstration of the receiver paramenters viz. sensitivity selectivity and fidelity. A kit for familiarization with phased lock loop (PLL) is also head. As at present mostly the data transmission is done by digital means, knowledge of these methods is very useful for engineering students .
In this lab various data communication techniques like analog (AM, FM & PM) and digital (ASK, FSK, BPSK & QPSK) are analyzed practically. Introduction to various media types for data transmission are also given in this lab. In communication lab techniques transmit data digitally are taught. We have MATLAB(SIMULINK)/ COMSIM software for digital link simulation, for error introduction & error estimation in digital link. In this lab we have various equipment to teach the students the basics concepts of digital electronics. To study logic gates we have TTL logic gates 7400, 7402, 7408, 7432,7486 ,74150 demultiplexer kits.Other than this IC-7483, 7476 JK Flipflops and 7474 D FlipFlops and Johnson counters.</p>
				
				<h4><li><b>Microprocessor and Advanced Microprocessor Lab</b></li></h4>

				<p>The lab is fully equiped with kits of 8085/8086. Interfacing cards like ADC, DAC ,dc motor,Trafic light Control,Stepper motors kits of various interfacing devices like 8255(PPI), 8259(Interrupt Controller),8279(keyboard controller) & 8237 (DMA Controller)</p>
				
				<h4><li><b>Basic Electrical Engineering Lab</b></li></h4>

				<p>This lab is equipped with the experimental kits for drawing the characterstics of SCR, MOSFET, FET,BJT TRAIC, DIAC, DC MOTOR and INDUCTION MOTOR in various configurations. The lab is also equipped with DYNA 1750 kit, the trainer kit LVDT, which helps the students in the study of transducer.</p>
				
				<h4><li><b>Digital Signal Processing Lab</b></li></h4>

				<p>In this student gets knowledge of MATLAB software. This include MATLAB, SIMULINK software, and Singnal-Processing toolbox, Signal Processing block set,Filter design toolbox & Control system toolbox. It also includes kits like ADSP 2181 Based DSP Kit (ADSP 2181, DSK Board).</p>
				
				<h4><li><b>Microwave & FTL Lab</b></li></h4>

				<p>Microwave have a broad rang of application in modern technology. Most important among them are is long distance communication system, radar astronomy, navigation etc. Microwave engineering lab is well equipped with advanced equipments Microwave (klystrom Oscillator) Test bench, Microwave (Gunn Oscillator) Test bench, Power Supply LS-303, Gunn diode power. We also perform practical of FTL. All equipment such as, low pass T type m-derived filters kits are available.</p>
				
				<h4><li><b>VHDL Programing Technology</b></li></h4>

				<p>The VHDL lab offers excellent oppurtunities for study in the areas of VLSI Design. Xilinx software and FPGA Kits are available.</p>
