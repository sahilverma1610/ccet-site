           <a name="history"><h1 style="font-family:Orbitron">HISTORY</h1></a>
		   <hr>
		   <p class="staticcontent" align="justify">Chandigarh College of Engineering and Technology (CCET)
Degree Wing, Chandigarh, a government institute under UT
Administration, Chandigarh, was established in 2002. CCET
(Degree Wing) has the reputation of being one of the premier
engineering college for imparting quality education in
Engineering & Technology which is located in the city beautiful
sector -26, Chandigarh. CCET (Degree Wing), Chandigarh, is
affiliated to Panjab University (PU), Sector 14, Chandigarh and
approved by All India Council for Technical Education
(AICTE), Ministry of HRD, Government, of India, New
Delhi. CCET is well known for its huge infrastructure and
state of the art facilities. It is also known as Green Campus
because it has a unique environment friendly & energy efficient
building with fabulous architecture, lush verdant woods and
landscape gardens that provide an idyllic academic environment
to our students. CCET offers B.E. (Bachelor of Engineering)
courses in four streams i.e. . Electronics &Communication
Engineering, Computer Science & Engineering, Mechanical
Engineering and Civil Engineering.<p>    
<p class="staticcontent" align="justify">
The college campus extends over an area of 32 acres (130,000 m2) of land situated close to the Shivalik ranges and Sukhna Lake. The campus is divided into zones like administration blocks for Degree and Diploma stream, and hostels and residential complex for faculty and staff.
In addition to lecture halls, tutorial rooms and drawing halls, the college has an auditorium, library, computer centers, workshops, laboratories and playgrounds. The college has a branch of State Bank of Patiala with ATM facility, extension counter of Post Office and a canteen. The campus has a unit of the National Cadet Corps (NCC).

</p><br>
