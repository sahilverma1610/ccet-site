  <div class="panel-body">The Institute Public Relationship Cell is dedicated to develop the healthy relationship between alumni, scholars, parents and general populace. It is very necessary to interact with these stakeholders to inform about students’ achievements and success for all round development of institute. To meet this objective, IPRC is very much committed to give real reports and feedback to these stakeholders. This cell is a mirror of the institution which presents various pictures of students’ achievements.
This cell acts as a interface to provide point of contact for different national media houses to cover different activities related to academic, cultural, literary and sports organized at the campus by different departments and offices. At the same time, it is prime duty of this cell to convince these media houses to pay attention and highlight these activities to their newspaper and online media. Through this, we can develop and strengthen the positive and productive relationship to national print and electronic media.

The media coverage includes the preparation of press releases in English, Hindi and Punjabi languages and their further transmission to the newspapers and online media along with concerned photographs. It makes arrangements for photography and videography of events and occasions and maintains a record of newspaper clippings.

It is responsibility of this cell to provide any types of guidance to any visiting guests and renowned dignitaries and clear any quires regarding any activities of institutions.
