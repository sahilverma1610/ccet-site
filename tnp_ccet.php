            <h1>Procedure</h1>
            
        <h3>Procedures</h3>
        <p> Placements at CCET are organized and conducted by The Training and Placement Cell with the assistance of students and faculty. Arrangements for pre-placement talks and interaction sessions could be made upon request. The recruitment process is as follows:</p>
        <br>
        <ul style="list-style-type:decimal; margin-left:30px;line-height:20px;font-size:16px">
          <li style="padding-bottom:10px">The company is encouraged to conduct pre-placement talks before any short-listing exams before the campus interview sessions would commence. Slots for such activities would be, however, allotted based on mutual convenience and availability.</li>
          <li style="padding-bottom:10px">Companies can register themselves with the TnP Cell of the College.</li>
          <li style="padding-bottom:10px">Companies can send in their job notification to the TnP Cell and they can also ask for the profile of students and view the resumes of the students who have registered for the placement drive.</li>
          <li style="padding-bottom:10px">The Placement cell allots the dates for the campus interviews but are negotiable by the company.</li>
          <li style="padding-bottom:10px">By the end of the scheduled day of campus interview, the company is required to declare a list of final selected students on the same day.</li>
          <li style="padding-bottom:10px">At the end of selection process, the company is required to send offer letters to the Placement Officer who will then hand them over to the concerned students.</li>
        </ul>
        <br>
        <p><b>Note</b>:&nbsp; The above rules are subject to change as and when the Training and Placement Cell deems fit. On all matters not covered by the above rules, the Training and Placement Cell shall act on its discretion and its decision would be binding on all parties.</p>
      
