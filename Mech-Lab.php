		   <p> Knowledge without practical application is of no use, at least for a Mechanical Engineer. For this we have all kinds of laboratories which include</p>
			<ul>
			
		  <h4><li><b>Computer-Aided Design:</b></li></h4>

			<p>The CAD laboratory of the college is one of the best in the region with state of the art software and hardware. CAD lab has the following software's:</p>
			<ol>
				<li>ANSYS</li>
