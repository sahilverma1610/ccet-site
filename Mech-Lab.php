				<h4><li><b>Applied Thermodynamics:</b></li></h4>

				<p>The applied thermodynamics lab has a number of models, cut sections and experimental setups to make the students conversant with actual engines and measurement of their performance. </p>
				
				<h4><li><b>Mechanics of Materials:</b></li></h4>

				<p>The objective of this lab is to provide know-how to evaluate the different mechanical properties of various materials. Advanced facilities like creep testing, fatigue testing, torsion testing, Hardness testing machines are available apart from the basic testing facilities. </p>
				
				<h4><li><b>Mechatronics:</b></li></h4>

				<p>This lab is a merger of mechanical instruments with computers and electronics .This gives an idea of hydraulics and pneumatics coupled with simulation kits. A complete set up of hydraulic training kit and pneumatic training experimental equipment is available.</p>
				
				<h4><li><b>Heat Transfer:</b></li></h4>

				<p>The purpose of this lab is to demonstrate the students about the different heat transfer processes like conduction, convection & radiation with the help of equipments and also to make them understand different heat exchangers, heat pipes practically. It also aims at calculating heat transfer coefficients, thermal conductivity and Stefan's Boltzmann's coefficient. This lab is equipped with complete setup for performing the experiments.</p>
				
				<h4><li><b>Fluid Mechanics and Machinery: </b></li></h4>

				<p>The purpose of this lab is to study the working and constructional features of fluid machinery like turbines, pumps and other hydraulic equipment. This laboratory is well equipped with the experimental setup to perform the experiments on fluid machinery and demonstrate the working of different machines. </p>
				<h4><li><b>Theory of Machines & Vibrations: </b></li></h4>

				<p>This lab is equipped with various working models to enable the students to understand the theory of machines & vibrations developed and the methods of their measurement.  </p>
				<h4><li><b>Theory of Machines & Vibrations: </b></li></h4>

				<p>This lab is equipped with various working models to enable the students to understand the theory of machines & vibrations developed and the methods of their measurement.  </p>

				<h4><li><b>Refrigeration & Air Conditioning:</b></li></h4>

				<p>This lab is equipped to perform the experiments including the performance and study of basic components used in Refrigeration and Air Conditioning. The equipments include Refrigeration Test Rig, Water Cooler, Simple Vapour Compression System, Domestic Refrigerator, Ice Plant, Vapour Absorption Refrigeration System, Blower Test Rig, Air Conditioner and Cut Sections of different components.   </p>

