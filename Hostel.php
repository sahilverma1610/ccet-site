</li>
<li class="list-group-item">2.	The residents are not allowed to gamble in the hostel premises. 
</li>

<li class="list-group-item">3	The residents are not allowed to bring intoxicants and take part in activities of intoxication in any from in the hostel premises. 
</li>
<li class="list-group-item">4.	The residents are not allowed to keep guests in their rooms, except with the prior written permission of the Hostel Warden. 
</li>
<li class="list-group-item">5.	The residents shall not commit any breach of peace in the hostel premises 
</li>
<li class="list-group-item">6.	The residents shall not invite undesirable elements into the hostel premises. 
</li>

<li class="list-group-item">7.	The residents are not permitted to stay overnight out of their hostels, or go out of station without prior permission of the Hostel Warden. 
</li>
<li class="list-group-item">8.	The residents are not permitted to install and use any electric appliance except a wall light, a table lamp and a table fan for their rooms. A breach of this rule result in the forfeiture of the appliance seized. 
</li>
<li class="list-group-item">9.	Residents are not permitted to keep with them any offensive weapon, such as fire arms, Kirpan or Khukhri, etc. Strict action will be taken against anyone found in possession of such a weapon. 
</li>
<li class="list-group-item">10.	A Hostel Discipline committee comprising of senior and nominated members shall assist the Hostel Warden in monitoring the discipline in the Hostel. 
</li>
<li class="list-group-item">11.	A resident shall not keep cash exceeding Rs. 500/- with him in room. Cash in excess of this shall be deposited with the accountant/Mess Manager. 

 </li>
<li class="list-group-item">12.	The residents are not allowed to keep costly items like T.V., Computers, Stereo decks etc. in their rooms. 
</li>
<li class="list-group-item">13	The allotment of seats rests with the Hostel warden No boarder is allowed to change his room or furniture without prior permission of the authorities. This will apply even if the change is mutual. 
</li>
<li class="list-group-item">14.	Students are expected to refrain from disfiguring walls, doors etc. and removal of glass panes. This will help towards better maintenance of the hostel. 
</li>
<li class="list-group-item">15.	Boarders, who are in arrears of any mess bill for more than a month, shall have to vacate their seats in the hostel. 
</li>
<li class="list-group-item">16.	Habitual defiance of any of the Hostel Rules/Instructions will render resident student liable to disciplinary action 
</li>
<li class="list-group-item">17	These rules may be amended, substituted, or deleted without notice.</li>

