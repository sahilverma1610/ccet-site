		   <p style="font-family:arial">  
		   Better interaction between institutions and industry is most important requirement in present scenario. This will have great bearing on the engineering research & project, exposure of industrial atmosphere to engineering students and subsequent placement of young graduating engineers in industries across the globe. With the advent of globalization and opening up of Indian economy to outside world, CCET (Degree Wing) started INDUSTRY-NSTITUTION RELATION CELL (IIRC) with aim to enhance globe repute industry and institute interaction with CCET students and minimize the gap between academia and industry for better exchange of their expertise resources to produce skill based nation building technical manpower. 
There is an urgent need to prepare engineering students for jobs in multinational companies and research organizations, by exposing them to newer technologies and engineering methodologies. These aims can only be achieved well by bridging the gap between industry and the academic institute. With this aim, IIRC invites scientist, Academic researchers, thought-leaders, entrepreneur, industry executive, policy maker and other renewed orator to address and share their expertise to students. In association with training & Placement department, IIRC also help and provide the training in the area of behavior, strategy, leadership, business knowledge, soft skill and communication development.
The IIRC also facilitates consultancy, sponsored R&D projects and industrial and academic trainings those are not prescribed in the syllabus in addition to conducting industrial exhibitions and interaction meets. This cell will enable the academia and industries in particular to know about the facilities on training, testing and research activities in the college's departments.
</p>
