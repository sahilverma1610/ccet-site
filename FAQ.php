<P class="p0 ft0">FAQ Regarding Ordinance Rule</P>
<P class="p20 ft1">Ques. What are the regulations issued by panjab university to earn B.E degree in any mentioned stream.</P>
<P class="p0 ft2">Ans.</P>
<P class="p21 ft10"><SPAN class="ft21">1.</SPAN><SPAN class="ft22">The duration of the course of instruction for Bachelor of Engineering in all disciplines being offered by the Panjab University, shall be Four years (comprising of eight semesters, with two semesters per year). Each semester shall be at least of fourteen weeks duration.</SPAN></P>
<P class="p22 ft13"><SPAN class="ft10">2.</SPAN><SPAN class="ft12">A student shall be eligible to appear in the examination only if he/she has attended at least 75% of the total classes held as mentioned above during the semester. The attendance shall be certified by the Chairperson of the University Department(s)/institutes/Director of Institute/Principal of College as the case may be.</SPAN></P>
