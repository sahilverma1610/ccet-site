<P class="p9 ft0">FAQ for Admission Process Procedure</P>
<P class="p10 ft9">Ques. What are the minimum eligibility criteria for admission?</P>
<P class="p0 ft10"><SPAN class="ft9">Ans. </SPAN>The admission criteria are as follows:</P>
<P class="p11 ft10"><SPAN class="ft10">1.</SPAN><SPAN class="ft11">The mode of admission to the First Semester course in any branch will be decided by the P.U. Syndicate. It will be open to a candidate, who has passed 10+2 examination of the Central Board of Secondary Education, New Delhi or its equivalent with Physics and Mathematics as compulsory subjects along with one of the following subjects : Chemistry, Biotechnology, Computer Science or Biology.</SPAN></P>
<P class="p12 ft13"><SPAN class="ft10">2.</SPAN><SPAN class="ft12">A candidate must have obtained a minimum of 60% marks in the qualifying examination i.e. +2 for admission to the first year B.E. and Integrated B.E. courses in all the University Engineering Departments and Colleges affiliated to it except in the case of SC/ST/Physically Handicapped categories for which the percentage shall be 55% for admission to Engineering courses. The candidates shall be admitted on the basis of JEE (Mains) merit conducted by CBSE.</SPAN></P>
<P class="p13 ft15"><SPAN class="ft10">3.</SPAN><SPAN class="ft14">The mode of admission to the Second year B.E Programme (lateral entry) where ever applicable will be decided by the P.U Syndicate from time to time. It will be open to a candidate who has passed</SPAN></P>
<P class="p14 ft13"><SPAN class="ft10">3</SPAN><SPAN class="ft16">year Diploma from the recognized State Board of Technical Education in India with 60% marks in the aggregate. The admission will only be made in the corresponding or equivalent branches of degree courses. Admission will be made on the basis of merit obtained in the Entrance Examination to be conducted by the Panjab University.</SPAN></P>
<P class="p15 ft17">Ques. If I do not get the preference of my choice, will it be possible to transfer my branch after first year?</P>
<P class="p16 ft19"><SPAN class="ft18">Ans. </SPAN>Yes, branch can be changed but it would be as per P.U guidelines amended time to time.</P>
